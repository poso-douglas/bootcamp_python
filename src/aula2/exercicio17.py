# Booleanos (bool)
# Escreva um programa que avalie duas expressões booleanas inseridas pelo usuário e retorne o resultado 
# da operação AND entre elas. 

b1 = True
b2 = False

resultado_and = b1 and b2
resultado_or = b1 or b2
resultado_not = not b1

print("b1 = True e b2 = False")
print(f"resultado_and {resultado_and}")
print(f"resultado_or {resultado_or}")
print(f"resultado_not {resultado_not}")
    